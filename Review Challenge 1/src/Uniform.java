
public class Uniform
	{
	private int number;
	private String name;
	
	public Uniform (int nu, String na)
		{
		number = nu;
		name = na;
		}

	public int getNumber()
		{
			return number;
		}

	public void setNumber(int number)
		{
			this.number = number;
		}

	public String getName()
		{
			return name;
		}

	public void setName(String name)
		{
			this.name = name;
		}
	
	}
